/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[]}
 */
var postorderTraversal = function (root) {
    let res = [];
    let temp = [];
    let node = root;
    while (node || temp.length !== 0) {
        while (node) {
            res.unshift(node.val);
            temp.push(node);
            node = node.right;
        }
        node = temp.pop();
        node = node.left;
    }
    return res;
};
