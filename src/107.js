/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {number[][]}
 */
var levelOrderBottom = function (root) {
    if (!root) {
        return [];
    }
    let res = [];
    let i = -1;
    function walk(node) {
        i++;
        if (node) {
            if (!res[i]) {
                res[i] = [node.val];
            } else {
                res[i].push(node.val);
            }
            walk(node.left);
            walk(node.right);
        }
        i--;
    }
    walk(root);
    return res.reverse();
};
